const mongoose = require("mongoose");

const docSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const DoctorDb = mongoose.model("Doctor", docSchema);

module.exports = DoctorDb;
